#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*ps;
	int		i;

	ps = (char*)s;
	i = -1;
	while (s[++i])
	{
		if (s[i] == (char)c)
			return (&ps[i]);
	}
	return (NULL);
}
